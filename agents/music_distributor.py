@app.post("/api/releases")
async def create_release(
    title: str = Query(...),
    artist_key: str = Query("hive_dynamics"),
    release_type: str = Query("single"),
    track_ids: str = Query("[]"),
    release_date: str = Query(""),
):
    """Create a release (single, EP, or album) grouping tracks."""
    con = get_db()
    now = datetime.now(timezone.utc).isoformat()
    upc = generate_upc()

    try:
        ids = json.loads(track_ids)
    except json.JSONDecodeError:
        ids = []

    # Generate cover art prompt for the release
    genre = ""
    if ids:
        track = con.execute("SELECT genre, mood FROM tracks WHERE id = ?", (ids[0],)).fetchone()
        if track:
            genre = track["genre"]

    cover_prompt = generate_cover_art_prompt(
        title, genre or "Electronic", "energetic", artist_key
    )

    con.execute("""
        INSERT INTO releases (
            title, artist_key, release_type, upc, release_date,
            cover_art_prompt, track_ids, status, created_at, updated_at
        ) VALUES (?, ?, ?, ?, ?, ?, ?, 'draft', ?, ?)
    """, (title, artist_key, release_type, upc, release_date,
          cover_prompt, json.dumps(ids), now, now))
    con.commit()
    con.close()

    return {
        "status": "created",
        "title": title,
        "release_type": release_type,
        "upc": upc,
        "cover_art_prompt": cover_prompt,
    }


