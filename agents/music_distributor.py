@app.get("/api/stats")
async def get_stats():
    """Get overall distribution statistics."""
    con = get_db()

    tracks_total = con.execute("SELECT COUNT(*) FROM tracks").fetchone()[0]
    tracks_new = con.execute("SELECT COUNT(*) FROM tracks WHERE status = 'new'").fetchone()[0]
    tracks_queued = con.execute("SELECT COUNT(*) FROM tracks WHERE status = 'queued'").fetchone()[0]
    tracks_distributed = con.execute("SELECT COUNT(*) FROM tracks WHERE status = 'distributed'").fetchone()[0]

    queue_pending = con.execute("SELECT COUNT(*) FROM distribution_queue WHERE status = 'pending'").fetchone()[0]
    queue_submitted = con.execute("SELECT COUNT(*) FROM distribution_queue WHERE status = 'submitted'").fetchone()[0]
    queue_live = con.execute("SELECT COUNT(*) FROM distribution_queue WHERE status = 'live'").fetchone()[0]

    total_streams = con.execute("SELECT COALESCE(SUM(streams), 0) FROM revenue").fetchone()[0]
    total_revenue = con.execute("SELECT COALESCE(SUM(revenue_usd), 0) FROM revenue").fetchone()[0]

    platforms_setup = con.execute(
        "SELECT COUNT(*) FROM platform_accounts WHERE setup_status != 'not_started'"
    ).fetchone()[0]

    # Per-artist breakdown
    artist_stats = {}
    for key in ARTISTS:
        count = con.execute("SELECT COUNT(*) FROM tracks WHERE artist_key = ?", (key,)).fetchone()[0]
        artist_stats[key] = {"name": ARTISTS[key]["name"], "tracks": count}

    con.close()

    return {
        "tracks": {
            "total": tracks_total,
            "new": tracks_new,
            "queued": tracks_queued,
            "distributed": tracks_distributed,
        },
        "distribution_queue": {
            "pending": queue_pending,
            "submitted": queue_submitted,
            "live": queue_live,
        },
        "revenue": {
            "total_streams": total_streams,
            "total_revenue_usd": round(total_revenue, 2),
        },
        "platforms_configured": platforms_setup,
        "total_platforms": len(PLATFORMS),
        "artists": artist_stats,
    }


