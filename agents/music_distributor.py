    query = "SELECT r.*, t.title, t.artist_name FROM revenue r JOIN tracks t ON t.id = r.track_id WHERE 1=1"
    params = []
    if platform:
        query += " AND r.platform = ?"
        params.append(platform)
    if period:
        query += " AND r.period = ?"
        params.append(period)
    if track_id:
        query += " AND r.track_id = ?"
        params.append(track_id)
    query += " ORDER BY r.period DESC, r.platform"
    records = [dict(r) for r in con.execute(query, params).fetchall()]

    con.close()

    return {
        "summary": {
            "total_streams": overall[0],
            "total_revenue_usd": round(overall[1], 2),
            "currency": "USD",
        },
        "by_platform": platform_summary,
        "records": records,
        "note": (
            "Revenue data is entered manually from platform dashboards until "
            "API integrations are built. Check Amuse app, Spotify for Artists, "
            "Bandcamp stats, and SoundCloud insights for latest numbers."
        ),
    }


