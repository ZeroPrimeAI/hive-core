def _seed_platform_accounts():
    """Seed platform_accounts table with known platforms."""
    con = get_db()
    now = datetime.now(timezone.utc).isoformat()
    for key, plat in PLATFORMS.items():
        existing = con.execute(
            "SELECT id FROM platform_accounts WHERE platform = ?", (key,)
        ).fetchone()
        if not existing:
            con.execute("""
                INSERT INTO platform_accounts (platform, setup_status, created_at, updated_at)
                VALUES (?, 'not_started', ?, ?)
            """, (key, now, now))
    con.commit()
    con.close()


