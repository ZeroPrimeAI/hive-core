def generate_isrc(country: str = "US", registrant: str = "HV1", year: int = None,
                  sequence: int = None) -> str:
    """
    Generate an ISRC (International Standard Recording Code).
    Format: CC-XXX-YY-NNNNN
      CC = country code (2 letters)
      XXX = registrant code (3 alphanumeric)
      YY = year (2 digits)
      NNNNN = designation code (5 digits)

    Note: Real ISRCs require registration with a national ISRC agency.
    Amuse/DistroKid auto-generate real ones. This generates placeholder format.
    """
    if year is None:
        year = datetime.now(timezone.utc).year % 100
    if sequence is None:
        sequence = random.randint(10000, 99999)
    return f"{country}{registrant}{year:02d}{sequence:05d}"


def generate_upc(prefix: str = "019529") -> str:
    """
    Generate a UPC-A barcode (12 digits).
    Format: PPPPPP-IIIII-C
      PPPPPP = company prefix (6 digits)
      IIIII = item number (5 digits)
      C = check digit

    Note: Real UPCs require GS1 registration ($250+/yr).
    Distributors (Amuse) auto-generate them for free.
    This generates placeholder format for tracking.
    """
    item = random.randint(10000, 99999)
    base = f"{prefix}{item}"
    # Calculate UPC check digit
    odd_sum = sum(int(base[i]) for i in range(0, 11, 2))
    even_sum = sum(int(base[i]) for i in range(1, 11, 2))
    check = (10 - ((odd_sum * 3 + even_sum) % 10)) % 10
    return f"{base}{check}"


