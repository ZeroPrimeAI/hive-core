@app.post("/api/distribute/{track_id}")
async def distribute_track(track_id: int, req: Optional[DistributeRequest] = None):
    """
    Mark a track for distribution to platforms.
    If no platforms specified, queues for ALL platforms.
    """
    con = get_db()
    track = con.execute("SELECT * FROM tracks WHERE id = ?", (track_id,)).fetchone()
    if not track:
        con.close()
        raise HTTPException(404, f"Track {track_id} not found")

    now = datetime.now(timezone.utc).isoformat()
    platforms = (req.platforms if req and req.platforms else list(PLATFORMS.keys()))
    notes = req.notes if req else ""

    queued = []
    skipped = []
    for plat in platforms:
        if plat not in PLATFORMS:
            skipped.append({"platform": plat, "reason": "unknown platform"})
            continue

        # Check if already queued
        existing = con.execute(
            "SELECT id, status FROM distribution_queue WHERE track_id = ? AND platform = ?",
            (track_id, plat)
        ).fetchone()
        if existing:
            skipped.append({"platform": plat, "reason": f"already {existing['status']}"})
            continue

        con.execute("""
            INSERT INTO distribution_queue (
                track_id, platform, status, notes, created_at, updated_at
            ) VALUES (?, ?, 'pending', ?, ?, ?)
        """, (track_id, plat, notes, now, now))
        queued.append(plat)

    # Update track status
    con.execute(
        "UPDATE tracks SET status = 'queued', updated_at = ? WHERE id = ?",
        (now, track_id)
    )
    con.commit()
    con.close()

    return {
        "status": "queued",
        "track_id": track_id,
        "track_title": track["title"],
        "queued_platforms": queued,
        "skipped": skipped,
        "next_steps": [
            f"Upload to {p}: see GET /api/platforms for setup steps" for p in queued
        ],
    }


@app.put("/api/queue/{queue_id}")
async def update_queue_item(queue_id: int, status: str = Query(...),
                             url: str = "", notes: str = ""):
    """Update distribution queue item status (pending -> submitted -> in_review -> live | failed)."""
    valid_statuses = ["pending", "submitted", "in_review", "live", "failed", "removed"]
    if status not in valid_statuses:
        raise HTTPException(400, f"Invalid status. Must be one of: {valid_statuses}")

    con = get_db()
    now = datetime.now(timezone.utc).isoformat()

    item = con.execute("SELECT * FROM distribution_queue WHERE id = ?", (queue_id,)).fetchone()
    if not item:
        con.close()
        raise HTTPException(404, f"Queue item {queue_id} not found")

    updates = {"status": status, "updated_at": now}
    if status == "submitted":
        updates["submitted_at"] = now
    if status == "live":
        updates["live_at"] = now
    if url:
        updates["url"] = url
    if notes:
        updates["notes"] = notes

    set_clause = ", ".join(f"{k} = ?" for k in updates)
    vals = list(updates.values()) + [queue_id]
    con.execute(f"UPDATE distribution_queue SET {set_clause} WHERE id = ?", vals)

    # If all platforms are live, update track status
    if status == "live":
        track_id = item["track_id"]
        pending = con.execute(
            "SELECT COUNT(*) FROM distribution_queue WHERE track_id = ? AND status != 'live'",
            (track_id,)
        ).fetchone()[0]
        if pending == 0:
            con.execute(
                "UPDATE tracks SET status = 'distributed', updated_at = ? WHERE id = ?",
                (now, track_id)
            )

    con.commit()
    con.close()
    return {"status": "updated", "queue_id": queue_id, "new_status": status}


