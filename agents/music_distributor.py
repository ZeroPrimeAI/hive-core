def generate_track_tags(genre: str, subgenre: str, mood: str,
                        artist_key: str) -> List[str]:
    """Generate platform-appropriate tags for a track."""
    base_tags = [genre.lower()]
    if subgenre:
        base_tags.append(subgenre.lower())
    if mood:
        base_tags.append(mood.lower())

    artist_info = ARTISTS.get(artist_key, ARTISTS["hive_dynamics"])
    base_tags.extend([g.lower() for g in artist_info["genres"][:3]])

    extra_tags = [
        "ai music", "ai generated", "neural network music",
        "electronic", "instrumental",
    ]
    if artist_key == "ghost_machine":
        extra_tags.extend(["anime", "anime ost", "japanese electronic",
                           "ghost in the machine"])
    else:
        extra_tags.extend(["hive dynamics", "ai beats", "computer generated"])

    all_tags = list(dict.fromkeys(base_tags + extra_tags))  # dedupe, preserve order
    return all_tags[:15]  # Most platforms cap at 10-15 tags


